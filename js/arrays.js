// Arrays (arreglos)

// Es una colección de valores (normalmente del mismo tipo) que nos permite simplificar las
// variables

var frutas = ['pera','manzana','naranja','plátano']; // declaración e inicialización de una array

console.log(frutas);

// Sintaxis de acceso al array con índice indexado a 0 (quiere decir es que la primera posición es 0)

console.log(frutas[1]);

frutas[2] = 'albaricoque';

console.log(frutas);

// Como los arrays son muy utilizados disponen de una colección de propiedades y métodos para
// manejarlos

console.log(frutas.length); // El número de elementos del array